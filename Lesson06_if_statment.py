# Here, we define the variables x and y. Then, we ask if x is greater than y. 
# If it is, then we will print 'x is greater than y.' 
# If it is not, then nothing will happen.

# In this case, 5 is not greater than 10, so nothing will happen.
x = 5
y = 10

if x > y:
    print('x is greater than y')
# Now, we've flipped the comparison operator, to say if x is less than y. 
# Basically, if 5 is less than 10. In this case, the statement is true, 
# and the output will be 'x is less than y.
x = 5
y = 10

if x < y:
    print('x is less than y')